#!/usr/bin/env python

class Printer(object):
    def __init__(self):
        pass

    def trickOrTreet(self):
        print("Trick-Or-Treat!!")

    def closed(self):
        print("closed connection")

    def onCount(self, trickOrTreatCount):
        print("Trick-Or-Treat count: {0}".format(trickOrTreatCount))

